// Decode the packed bitmap once at module load (synchronous, browser atob).
const LAND_MASK: Uint8Array = (() => {
  const bin = typeof atob !== "undefined" ? atob(LAND_MASK_B64) : "";
  const bytes = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) bytes[i] = bin.charCodeAt(i);
  return bytes;
})();

function isLandCell(row: number, col: number): boolean {
  const idx = row * GRID_COLS + col;
  return (LAND_MASK[idx >> 3] & (1 << (idx & 7))) !== 0;
}

function getDayOfYear(d: Date): number {
  const start = new Date(d.getUTCFullYear(), 0, 0);
  return Math.floor((d.getTime() - start.getTime()) / 86400000);
}

function getSolar(date: Date) {
  const dayOfYear = getDayOfYear(date);
  const declRad =
    (23.45 * Math.sin((2 * Math.PI / 365) * (dayOfYear - 81))) * Math.PI / 180;
  const utcHours =
    date.getUTCHours() +
    date.getUTCMinutes() / 60 +
    date.getUTCSeconds() / 3600;
  let subLonDeg = 15 * (12 - utcHours);
  subLonDeg = ((subLonDeg + 180) % 360 + 360) % 360 - 180;
  return { declRad, subLonDeg };
}

function getDaylight(latDeg: number, lonDeg: number, declRad: number, subLonDeg: number): number {
  const latRad = latDeg * Math.PI / 180;
  const lonRad = lonDeg * Math.PI / 180;
  const subLonRad = subLonDeg * Math.PI / 180;

  const cosZenith =
    Math.sin(latRad) * Math.sin(declRad) +
    Math.cos(latRad) * Math.cos(declRad) * Math.cos(lonRad - subLonRad);

  return Math.max(0, Math.min(1, cosZenith * 4 + 0.5));
}

const LAT_TOP = 83;
const LAT_BOTTOM = -58;
const LAT_SPAN = LAT_TOP - LAT_BOTTOM;

function colToLon(col: number) {
  return -180 + (col / (GRID_COLS - 1)) * 360;
}
function rowToLat(row: number) {
  return LAT_TOP - (row / (GRID_ROWS - 1)) * LAT_SPAN;
}
function lonToX(lon: number, W: number) {
  return ((lon + 180) / 360) * W;
}
function latToY(lat: number, H: number) {
  return ((LAT_TOP - lat) / LAT_SPAN) * H;
}

// Background wash colors
const DAY_BG = { r: 255, g: 236, b: 205 };    // warm orange
const NIGHT_BG = { r: 233, g: 228, b: 252 };  // soft violet
// Land dot colors
const DAY_DOT = { r: 150, g: 134, b: 110 };   // warm grey
const NIGHT_DOT = { r: 140, g: 132, b: 182 }; // cool violet-grey

export default function WorldMap({
  people = [],
  compact = false,
}: {
  people?: MapPerson[];
  compact?: boolean;
}) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const [times, setTimes] = useState<Record<string, string>>({});
  const [personDay, setPersonDay] = useState<Record<string, boolean>>({});

  const peopleKey = people.map((p) => `${p.name}@${p.tz}`).join("|");

  // Group people who sit on (essentially) the same spot into one cluster so
  // their markers and labels don't stack on top of each other.
  const clusters = useMemo(() => {
    const map = new Map<
      string,
      { lat: number; lon: number; tz: string; flag?: string; members: MapPerson[]; key: string }
    >();
    for (const p of people) {
      const key = `${p.lat.toFixed(1)}_${p.lon.toFixed(1)}`;
      let c = map.get(key);
      if (!c) {
        c = { lat: p.lat, lon: p.lon, tz: p.tz, flag: p.flag, members: [], key };
        map.set(key, c);
      }
      c.members.push(p);
    }
    return [...map.values()];
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [peopleKey]);

  // Update clocks every second (one entry per cluster — members share a spot)
  useEffect(() => {
    const update = () => {
      const t: Record<string, string> = {};
      const d: Record<string, boolean> = {};
      const date = new Date();
      const { declRad, subLonDeg } = getSolar(date);
      clusters.forEach((c) => {
        t[c.key] = getTimeInTimezone(c.tz);
        d[c.key] = getDaylight(c.lat, c.lon, declRad, subLonDeg) > 0.5;
      });
      setTimes(t);
      setPersonDay(d);
    };
    update();
    const id = setInterval(update, 1000);
    return () => clearInterval(id);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [peopleKey]);

  // Draw the dot-matrix map
  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    const W = canvas.width;
    const H = canvas.height;
    const cellW = W / GRID_COLS;
    const cellH = H / GRID_ROWS;
    const DOT_R = Math.min(cellW, cellH) * 0.42;

    function render() {
      if (!ctx) return;
      ctx.clearRect(0, 0, W, H);

      const date = new Date();
      const { declRad, subLonDeg } = getSolar(date);

      // ── 1. Background wash: warm day side / violet night side ──
      const BG_COLS = 120;
      const BG_ROWS = 60;
      const bw = W / BG_COLS;
      const bh = H / BG_ROWS;
      for (let row = 0; row < BG_ROWS; row++) {
        const lat = LAT_TOP - (row / (BG_ROWS - 1)) * LAT_SPAN;
        for (let col = 0; col < BG_COLS; col++) {
          const lon = -180 + (col / (BG_COLS - 1)) * 360;
          const day = getDaylight(lat, lon, declRad, subLonDeg);
          const r = Math.round(NIGHT_BG.r + (DAY_BG.r - NIGHT_BG.r) * day);
          const g = Math.round(NIGHT_BG.g + (DAY_BG.g - NIGHT_BG.g) * day);
          const b = Math.round(NIGHT_BG.b + (DAY_BG.b - NIGHT_BG.b) * day);
          ctx.fillStyle = `rgb(${r},${g},${b})`;
          ctx.fillRect(col * bw, row * bh, bw + 1, bh + 1);
        }
      }

      // ── 2. Sun glow ──
      const sunX = lonToX(subLonDeg, W);
      const sunY = H * 0.16;
      const glow = ctx.createRadialGradient(sunX, sunY, 0, sunX, sunY, W * 0.32);
      glow.addColorStop(0, "rgba(255,205,120,0.5)");
      glow.addColorStop(1, "rgba(255,205,120,0)");
      ctx.fillStyle = glow;
      ctx.fillRect(0, 0, W, H);

      // ── 3. Land dots tinted by day/night ──
      for (let row = 0; row < GRID_ROWS; row++) {
        const lat = rowToLat(row);
        const y = (row + 0.5) * cellH;
        for (let col = 0; col < GRID_COLS; col++) {
          if (!isLandCell(row, col)) continue;
          const lon = colToLon(col);
          const x = (col + 0.5) * cellW;
          const day = getDaylight(lat, lon, declRad, subLonDeg);

          const r = Math.round(NIGHT_DOT.r + (DAY_DOT.r - NIGHT_DOT.r) * day);
          const g = Math.round(NIGHT_DOT.g + (DAY_DOT.g - NIGHT_DOT.g) * day);
          const b = Math.round(NIGHT_DOT.b + (DAY_DOT.b - NIGHT_DOT.b) * day);

          ctx.beginPath();
          ctx.arc(x, y, DOT_R, 0, 2 * Math.PI);
          ctx.fillStyle = `rgba(${r},${g},${b},0.92)`;
          ctx.fill();
        }
      }

      // ── 4. Terminator (curved dashed day/night boundary) ──
      const tanDecl = Math.tan(declRad);
      ctx.beginPath();
      let started = false;
      for (let lon = -180; lon <= 180; lon += 1) {
        const Hrad = (lon - subLonDeg) * Math.PI / 180;
        let latRad: number;
        if (Math.abs(tanDecl) < 1e-4) {
          latRad = 0;
        } else {
          latRad = Math.atan(-Math.cos(Hrad) / tanDecl);
        }
        const lat = latRad * 180 / Math.PI;
        if (lat > LAT_TOP || lat < LAT_BOTTOM) {
          started = false;
          continue;
        }
        const x = lonToX(lon, W);
        const y = latToY(lat, H);
        if (!started) {
          ctx.moveTo(x, y);
          started = true;
        } else {
          ctx.lineTo(x, y);
        }
      }
      ctx.setLineDash([6, 7]);
      ctx.lineWidth = 2;
      ctx.strokeStyle = "rgba(146,120,190,0.5)";
      ctx.stroke();
      ctx.setLineDash([]);

      // ── 5. Person markers (one per cluster, with a count badge) ──
      for (const c of clusters) {
        const color = colorForName(c.members[0].name);
        const cx = lonToX(c.lon, W);
        const cy = latToY(c.lat, H);

        ctx.beginPath();
        ctx.arc(cx, cy, 13, 0, 2 * Math.PI);
        ctx.fillStyle = color + "26";
        ctx.fill();

        ctx.beginPath();
        ctx.arc(cx, cy, 5.5, 0, 2 * Math.PI);
        ctx.fillStyle = color;
        ctx.fill();
        ctx.strokeStyle = "#fff";
        ctx.lineWidth = 2;
        ctx.stroke();

        if (c.members.length > 1) {
          const bx = cx + 9;
          const by = cy - 9;
          ctx.beginPath();
          ctx.arc(bx, by, 7.5, 0, 2 * Math.PI);
          ctx.fillStyle = color;
          ctx.fill();
          ctx.strokeStyle = "#fff";
          ctx.lineWidth = 1.5;
          ctx.stroke();
          ctx.fillStyle = "#fff";
          ctx.font = "bold 10px system-ui, sans-serif";
          ctx.textAlign = "center";
          ctx.textBaseline = "middle";
          ctx.fillText(String(c.members.length), bx, by + 0.5);
          ctx.textAlign = "start";
          ctx.textBaseline = "alphabetic";
        }
      }
    }

    render();
    const id = setInterval(render, 30000);
    return () => clearInterval(id);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [peopleKey]);

  return (
    <div
      className={`relative overflow-hidden border border-white/60 bg-gradient-to-br from-orange-50 to-violet-50 ${
        compact ? "rounded-2xl" : "rounded-3xl shadow-sm mb-5"
      }`}
    >
      <canvas
        ref={canvasRef}
        width={1000}
        height={470}
        className="w-full block"
      />

      {/* Cluster labels — hidden in compact mode (too cramped) */}
      {!compact && clusters.map((c) => {
        const x = ((c.lon + 180) / 360) * 100;
        const y = ((LAT_TOP - c.lat) / LAT_SPAN) * 100;
        const isDay = personDay[c.key];
        const time = times[c.key] || "--:--";
        const multi = c.members.length > 1;
        const offset = multi ? 30 + c.members.length * 18 : 42;
        return (
          <div
            key={c.key}
            className="absolute -translate-x-1/2 flex items-start gap-1.5 whitespace-nowrap pointer-events-none"
            style={{ left: `${x}%`, top: `calc(${y}% - ${offset}px)` }}
          >
            {multi ? (
              <span className="flex flex-col gap-1 px-2.5 py-1.5 rounded-2xl bg-white/90 backdrop-blur-sm shadow-sm ring-1 ring-black/5">
                <span className="flex items-center gap-1.5">
                  <span className="text-xs font-semibold text-gray-700">
                    {c.flag ? `${c.flag} ` : ""}
                    {time}
                  </span>
                  <span
                    className={`text-base leading-none select-none ${
                      isDay
                        ? "drop-shadow-[0_2px_6px_rgba(251,191,36,0.6)]"
                        : "drop-shadow-[0_2px_6px_rgba(124,58,237,0.45)]"
                    }`}
                  >
                    {isDay ? "☀️" : "🌙"}
                  </span>
                </span>
                {c.members.map((m) => (
                  <span key={m.name} className="flex items-center gap-1.5">
                    <span
                      className="w-2 h-2 rounded-full flex-shrink-0"
                      style={{ backgroundColor: colorForName(m.name) }}
                    />
                    <span className="text-xs font-medium text-gray-600">{m.name}</span>
                  </span>
                ))}
              </span>
            ) : (
              <>
                <span className="flex items-center gap-1.5 px-2.5 py-1 rounded-full bg-white/90 backdrop-blur-sm shadow-sm ring-1 ring-black/5">
                  <span
                    className="w-2 h-2 rounded-full flex-shrink-0"
                    style={{ backgroundColor: colorForName(c.members[0].name) }}
                  />
                  <span className="text-xs font-semibold text-gray-700">
                    {c.flag ? `${c.flag} ` : ""}
                    {c.members[0].name}
                  </span>
                  <span className="text-[11px] text-gray-400 tabular-nums">{time}</span>
                </span>
                <span
                  className={`text-xl leading-none select-none ${
                    isDay
                      ? "drop-shadow-[0_2px_6px_rgba(251,191,36,0.6)]"
                      : "drop-shadow-[0_2px_6px_rgba(124,58,237,0.45)]"
                  }`}
                >
                  {isDay ? "☀️" : "🌙"}
                </span>
              </>
            )}
          </div>
        );
      })}
    </div>
  );
}
